'use client';

/**
 * Regulatory Reporting — Approvals (checker queue). Lists pending_approval
 * packages and hosts the decide panel. Maker-checker demo affordance: an
 * "Acting as" select feeds the X-User-Id override so a second officer can
 * decide; choosing the session user (the generator) surfaces the backend's
 * same-user 409 verbatim — that IS the maker-checker rule.
 */

import { useState } from 'react';
import { CheckCircle2, Loader2, UserCheck, XCircle } from 'lucide-react';
import type { RegulatoryPackageSummaryRead } from '@aequoros/risk-service-api';
import PageHeader from '@/components/ui/PageHeader';
import DataTable, { type Column } from '@/components/ui/DataTable';
import SectionCard from '@/components/ui/SectionCard';
import StatusPill from '@/components/ui/StatusPill';
import QueryBoundary, { ErrorPanel } from '@/components/ui/QueryBoundary';
import EmptyState from '@/components/ui/EmptyState';
import { SkeletonTable } from '@/components/ui/Skeleton';
import { useBankContext } from '@/components/shell/BankContext';
import {
  useDecidePackageApproval,
  useRegulatoryPackage,
  useRegulatoryPackages,
} from '@/lib/api/hooks';
import { fmtDateUTC, fmtTimestamp, shortId } from '@/lib/api/values';
import {
  DEMO_OFFICERS,
  FAMILY_LABELS,
  officerName,
} from '@/components/submissions/shared';

export default function ApprovalsPage() {
  const { bank } = useBankContext();
  const bankId = bank?.id;

  const queueQuery = useRegulatoryPackages(bankId, {
    status: 'pending_approval',
    includeSuperseded: false,
    limit: 100,
  });
  const queue = queueQuery.data?.packages ?? [];

  const [selectedId, setSelectedId] = useState<string | null>(null);
  const selected =
    queue.find((pkg) => pkg.id === selectedId) ?? queue[0] ?? null;

  const columns: Column<RegulatoryPackageSummaryRead>[] = [
    {
      key: 'return',
      header: 'Return',
      render: (pkg) => (
        <span className="font-mono text-caption font-medium text-navy">
          {pkg.returnCode}
        </span>
      ),
    },
    {
      key: 'family',
      header: 'Family',
      render: (pkg) => (
        <span className="text-caption text-slate">
          {FAMILY_LABELS[pkg.returnFamily] ?? pkg.returnFamily}
        </span>
      ),
    },
    {
      key: 'reportingDate',
      header: 'Reporting date',
      render: (pkg) => (
        <span className="font-mono text-caption text-navy/85 tnum">
          {fmtDateUTC(pkg.reportingDate)}
        </span>
      ),
    },
    {
      key: 'version',
      header: 'Version',
      numeric: true,
      render: (pkg) => `v${pkg.version}`,
    },
    {
      key: 'generatedBy',
      header: 'Generated by (maker)',
      render: (pkg) => (
        <span className="text-caption text-navy/85">
          {officerName(pkg.generatedBy)}
        </span>
      ),
    },
    {
      key: 'generatedAt',
      header: 'Generated',
      render: (pkg) => (
        <span className="font-mono text-micro text-slate tnum">
          {fmtTimestamp(pkg.generatedAt)}
        </span>
      ),
    },
    {
      key: 'validation',
      header: 'Validation',
      render: (pkg) =>
        pkg.validationPassed ? (
          <StatusPill tone="success">Passed</StatusPill>
        ) : (
          <StatusPill tone="critical">Failed</StatusPill>
        ),
    },
  ];

  return (
    <>
      <PageHeader
        breadcrumbs={[
          { label: 'Governance', href: '/submissions' },
          { label: 'Regulatory Reporting', href: '/submissions' },
          { label: 'Approvals' },
        ]}
        title="Approvals"
        subtitle="Checker queue — no package reaches a submission channel without approval by a different officer than its maker"
      />

      <div className="px-8 py-6 space-y-6">
        <QueryBoundary
          isLoading={queueQuery.isLoading}
          error={queueQuery.error}
          onRetry={() => queueQuery.refetch()}
          skeleton={
            <div className="card">
              <SkeletonTable rows={4} />
            </div>
          }
        >
          <SectionCard
            title="Pending approval"
            subtitle={`${queue.length} package${queue.length === 1 ? '' : 's'} awaiting a checker decision`}
            noPadding
          >
            {queue.length === 0 ? (
              <div className="p-5">
                <EmptyState
                  Icon={UserCheck}
                  title="Queue is clear"
                  description="Validated packages appear here once their maker requests approval from the Returns workspace."
                />
              </div>
            ) : (
              <DataTable
                columns={columns}
                rows={queue}
                density="compact"
                onRowClick={(pkg) => setSelectedId(pkg.id)}
                rowClassName={(pkg) =>
                  pkg.id === selected?.id ? 'bg-action-light/40' : ''
                }
              />
            )}
          </SectionCard>

          {selected && <DecidePanel bankId={bankId!} pkg={selected} />}
        </QueryBoundary>
      </div>
    </>
  );
}

function DecidePanel({
  bankId,
  pkg,
}: {
  bankId: string;
  pkg: RegulatoryPackageSummaryRead;
}) {
  const decide = useDecidePackageApproval(bankId);
  const detailQuery = useRegulatoryPackage(bankId, pkg.id);
  const [actingUserId, setActingUserId] = useState(DEMO_OFFICERS[0].id);
  const [reason, setReason] = useState('');

  const rejectNeedsReason = reason.trim().length === 0;
  const requestReason = detailQuery.data?.approvals.find(
    (approval) => approval.action === 'requested'
  )?.reason;

  return (
    <SectionCard
      title={
        <span className="inline-flex items-center gap-2">
          Decide — <span className="font-mono">{pkg.returnCode}</span>{' '}
          {fmtDateUTC(pkg.reportingDate)}{' '}
          <span className="font-mono text-caption text-slate tnum">
            v{pkg.version}
          </span>
        </span>
      }
      subtitle={`Maker: ${officerName(pkg.generatedBy)} · package ${shortId(pkg.id, 8)}`}
    >
      <div className="space-y-4 max-w-2xl">
        {requestReason && (
          <p className="text-caption text-navy/80 rounded border border-border-light bg-surface px-3 py-2">
            Maker&apos;s note: {requestReason}
          </p>
        )}

        <div>
          <label className="block text-caption font-medium text-navy mb-1.5">
            Acting as
          </label>
          <select
            value={actingUserId}
            onChange={(e) => setActingUserId(e.target.value)}
            className="w-full rounded border border-border bg-surface-raised px-2.5 py-2 text-body text-navy"
          >
            {DEMO_OFFICERS.map((officer) => (
              <option key={officer.id} value={officer.id}>
                {officer.name} — {officer.role}
              </option>
            ))}
          </select>
          <p className="mt-1.5 text-caption text-slate leading-relaxed">
            Demo: acting as a second officer — production uses your login.
            Deciding as the maker is rejected by the backend&apos;s
            maker-checker rule.
          </p>
        </div>

        <div>
          <label className="block text-caption font-medium text-navy mb-1.5">
            Reason{' '}
            <span className="font-normal text-slate">(required to reject)</span>
          </label>
          <textarea
            value={reason}
            onChange={(e) => setReason(e.target.value)}
            rows={2}
            placeholder="e.g. Cross-checked HQLA stock against the buffer dashboard."
            className="w-full rounded border border-border bg-surface-raised px-2.5 py-2 text-body text-navy placeholder:text-slate-light"
          />
        </div>

        <div className="flex items-center gap-2">
          <button
            type="button"
            disabled={decide.isPending}
            onClick={() =>
              decide.mutate({
                packageId: pkg.id,
                action: 'approved',
                reason: reason.trim() || undefined,
                actingUserId,
              })
            }
            className="inline-flex items-center gap-1.5 px-3 py-2 text-caption font-medium btn-primary disabled:opacity-60"
          >
            {decide.isPending ? (
              <Loader2 size={13} className="animate-spin" aria-hidden />
            ) : (
              <CheckCircle2 size={13} aria-hidden />
            )}
            Approve
          </button>
          <button
            type="button"
            disabled={decide.isPending || rejectNeedsReason}
            title={rejectNeedsReason ? 'A reason is required when rejecting.' : undefined}
            onClick={() =>
              decide.mutate({
                packageId: pkg.id,
                action: 'rejected',
                reason: reason.trim(),
                actingUserId,
              })
            }
            className="inline-flex items-center gap-1.5 px-3 py-2 text-caption font-medium text-critical border border-critical/30 bg-critical-light/40 rounded-md hover:bg-critical-light disabled:opacity-60"
          >
            <XCircle size={13} aria-hidden />
            Reject (rework)
          </button>
        </div>

        {decide.error && (
          <ErrorPanel error={decide.error} title="Decision rejected" />
        )}
        {decide.isSuccess && (
          <p className="text-caption text-success font-medium">
            Decision recorded — the package moved to &apos;
            {decide.data.status}&apos;.
          </p>
        )}
      </div>
    </SectionCard>
  );
}
