def _liquidity_sections(*, lmt_subset: bool) -> tuple[SectionLayout, ...]:
    """Section layouts shared by BSD3 and the LMT return.

    The generator emits the same snapshot for both; the LMT template renders
    only the LCR-tool subset (LMTD ¶39–43, Table 11) because the snapshot
    honestly carries no contractual-maturity-mismatch, funding-concentration
    or unencumbered-asset data for LMTD Tables 1–10 (see TODO(RR-6) note).
    """
    lcr = (
        SectionLayout(
            section_code="hqla",
            layout_id="hqla_representative",
            sheet_title="Stock of HQLA",
            columns=(_CODE, _ITEM, _AMOUNT),
            fidelity="PARTIAL",
            source_citation=_LCR_GAP_CITATION,
            notes=(
                "Row taxonomy follows LMTD Table 11 'Stock of HQLA — Level 1; Level 2A; "
                "Level 2B' (CONFIRMED); haircuts are Basel-default pending the unpublished "
                "LCR Directive calibration.",
            ),
        ),
        SectionLayout(
            section_code="outflows",
            layout_id="outflows_representative",
            sheet_title="Cash Outflows (30 days)",
            columns=_WEIGHTED_COLUMNS,
            fidelity="REPRESENTATIVE",
            source_citation=_LCR_GAP_CITATION,
            notes=(
                "Run-off rates are Basel-default; BoG rates live in the unpublished "
                "LCR Directive (gap G1).",
            ),
        ),
        SectionLayout(
            section_code="inflows",
            layout_id="inflows_representative",
            sheet_title="Cash Inflows (30 days)",
            columns=_WEIGHTED_COLUMNS,
            fidelity="REPRESENTATIVE",
            source_citation=_LCR_GAP_CITATION,
            notes=("Inflow rates and the 75% cap are Basel-default (gap G1).",),
        ),
        SectionLayout(
            section_code="lcr_summary",
            layout_id="lcr_summary_partial",
            sheet_title="Liquidity Coverage Ratio Summary",
            columns=_SUMMARY_COLUMNS,
            fidelity="PARTIAL",
            source_citation=_LMTD_CITATION,
            notes=(
                "Summary row language mirrors LMTD Table 11: 'A) Total adjusted HQLA', "
                "'B) Net Cash Outflow (2-1)', 'Liquidity Coverage Ratio (LCR) (A/B*100)' "
                "(CONFIRMED). Table 11 is per significant currency (Cedi | USD | Pound | "
                "Euro | Others); the snapshot carries the aggregate only — per-currency "
                "columns are not fabricated.",
            ),
        ),
    )
    if lmt_subset:
        return lcr
    nsfr = (
        SectionLayout(
            section_code="nsfr_asf",
            layout_id="nsfr_asf_representative",
            sheet_title="Available Stable Funding",
            columns=_WEIGHTED_COLUMNS,
            fidelity="REPRESENTATIVE",
            source_citation=_NSFR_CITATION,
        ),
        SectionLayout(
            section_code="nsfr_rsf",
            layout_id="nsfr_rsf_representative",
            sheet_title="Required Stable Funding",
            columns=_WEIGHTED_COLUMNS,
            fidelity="REPRESENTATIVE",
            source_citation=_NSFR_CITATION,
        ),
        SectionLayout(
            section_code="nsfr_summary",
            layout_id="nsfr_summary_representative",
            sheet_title="Net Stable Funding Ratio Summary",
            columns=_SUMMARY_COLUMNS,
            fidelity="REPRESENTATIVE",
            source_citation=_NSFR_CITATION,
        ),
    )
    return (*lcr, *nsfr)


