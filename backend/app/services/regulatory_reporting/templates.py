"""Declarative BoG return templates (docs/regulatory_reporting.md §5, exports).

Each :class:`ReturnTemplate` describes how one registered return renders from
its ``regulatory-package-v1`` snapshot: the metadata/header block (institution,
reporting date, ``Amounts in GHS '000`` unit convention, solo/consolidated
basis, preparer/approver attestation lines), the per-section sheet layouts,
and per-section fidelity + source citations.

Fidelity honesty rules (spec §1 principle 4, research dossier
``docs/research/bog_returns_and_templates.md``):

- Column headers are transcribed from official BoG appendices only where the
  research captured them verbatim (``CONFIRMED``); everything else is a
  professional reconstruction whose ``layout_id`` ends in ``_representative``
  and whose ``fidelity`` says so. Nothing invented is passed off as official.
- Metadata conventions come from research §11: ``Amounts in GHS '000``,
  parenthesised negatives, T vs T−1 comparatives (not yet carried by the
  snapshot — flagged, never fabricated), solo/consolidated basis, Board-level
  attestation lines.

The resolver (:func:`build_rendered_return`) turns template + snapshot into a
format-neutral render model consumed by ``exports/{xlsx,csv,pdf}.py`` so all
three writers show exactly the same numbers.
"""

