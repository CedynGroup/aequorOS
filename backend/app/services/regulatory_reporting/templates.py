type ColumnKind = Literal["text", "ghs", "pct", "number", "bool", "auto"]

CURRENCY_UNIT_NOTE = "Amounts in GHS '000"
CURRENCY_UNIT_DIVISOR = Decimal("1000")
SOLO_BASIS = "Solo (standalone licensed entity — Act 930 s.91(1); CRD also requires consolidated)"
GENERATED_BY_LINE = "Generated by AequorOS Regulatory Reporting"

# Attestation conventions per research §11: ICAAP is submitted with Board
# resolutions and Senior Management reports; the LAS is a Board statement;
# monthly data returns carry preparer/approver sign-off. ORASS submissions
# authenticate at the portal — these lines cover the artifact itself.
STANDARD_ATTESTATION_LINES: tuple[str, ...] = (
    "Prepared by (name / designation / signature / date): ",
    "Reviewed and approved by (name / designation / signature / date): ",
    "We attest that this return is complete and accurate to the best of our "
    "knowledge (Act 930 s.93(3) applies to inaccurate or incomplete submissions).",
)
BOARD_ATTESTATION_LINES: tuple[str, ...] = (
    *STANDARD_ATTESTATION_LINES,
    "Accompanied by Board resolutions and Senior Management reports (ICAAP Guideline 2026 ¶71).",
)


@dataclass(frozen=True)
class ColumnSpec:
    """One rendered column: which row key it reads and how to format it.

    ``kind='auto'`` resolves per row from the row's own ``unit`` field
    (``ghs`` → thousands-scaled amount, ``pct`` → percentage) — used for
    summary sections that mix amounts and ratios.
    """

    key: str
    header: str
    kind: ColumnKind = "text"


@dataclass(frozen=True)
class SectionLayout:
    """How one snapshot section renders as a sheet/table."""

    section_code: str
    layout_id: str
    sheet_title: str
    columns: tuple[ColumnSpec, ...]
    fidelity: FidelityGrade
    source_citation: str
    notes: tuple[str, ...] = ()
    optional: bool = False


@dataclass(frozen=True)
class ReturnTemplate:
    """The full declarative rendering spec for one registered return."""

    template_id: str
    return_code: str
    title: str
    fidelity: FidelityGrade
    source_citation: str
    sections: tuple[SectionLayout, ...]
    currency_unit: str = CURRENCY_UNIT_NOTE
    basis: str = SOLO_BASIS
    attestation_lines: tuple[str, ...] = STANDARD_ATTESTATION_LINES
    notes: tuple[str, ...] = ()


