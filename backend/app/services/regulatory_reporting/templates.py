# ---------------------------------------------------------------------------
# Render model — format-neutral output consumed by xlsx/csv/pdf writers
# ---------------------------------------------------------------------------


@dataclass(frozen=True)
class RenderedCell:
    """One resolved cell: the display kind plus the typed value."""

    kind: ColumnKind
    value: Decimal | str | bool | None


@dataclass(frozen=True)
class RenderedRow:
    cells: tuple[RenderedCell, ...]
    is_total: bool = False


@dataclass(frozen=True)
class RenderedSection:
    layout: SectionLayout
    title: str
    rows: tuple[RenderedRow, ...]
    total_row: RenderedRow | None


@dataclass(frozen=True)
class RenderedReturn:
    """Everything the three writers need, resolved once from the snapshot."""

    template: ReturnTemplate
    metadata_pairs: tuple[tuple[str, str], ...]
    sections: tuple[RenderedSection, ...]
    # (module, run_id, input_hash, engine_version) per source run.
    provenance_runs: tuple[tuple[str, str, str, str], ...]
    provenance_lines: tuple[str, ...]
    attestation_lines: tuple[str, ...] = field(default=STANDARD_ATTESTATION_LINES)


def _missing_section_error(template_id: str, section_code: str) -> HTTPException:
    return HTTPException(
        status_code=status.HTTP_409_CONFLICT,
        detail={
            "error_code": "snapshot_section_missing",
            "message": (
                f"The package snapshot has no '{section_code}' section required by "
                f"template '{template_id}'. Regenerate the package before exporting."
            ),
        },
    )


def _bad_value_error(section_code: str, key: str, raw: Any) -> HTTPException:
    return HTTPException(
        status_code=status.HTTP_409_CONFLICT,
        detail={
            "error_code": "snapshot_value_not_numeric",
            "message": (
                f"Snapshot section '{section_code}' carries a non-numeric value "
                f"{raw!r} for '{key}'; the package cannot be exported faithfully."
            ),
        },
    )


def _resolve_cell(column: ColumnSpec, row: dict[str, Any], section_code: str) -> RenderedCell:
    raw = row.get(column.key)
    kind = column.kind
    if kind == "auto":
        kind = "pct" if row.get("unit") == "pct" else "ghs"
    if raw is None:
        return RenderedCell(kind=kind, value=None)
    if kind == "bool":
        return RenderedCell(kind="bool", value=bool(raw))
    if kind == "text":
        return RenderedCell(kind="text", value=str(raw))
    try:
        number = Decimal(str(raw))
    except InvalidOperation as exc:
        raise _bad_value_error(section_code, column.key, raw) from exc
    if kind == "ghs":
        return RenderedCell(kind="ghs", value=number / CURRENCY_UNIT_DIVISOR)
    return RenderedCell(kind=kind, value=number)


def _resolve_total_row(layout: SectionLayout, section: dict[str, Any]) -> RenderedRow | None:
    total = section.get("total")
    if total is None:
        return None
    cells: list[RenderedCell] = []
    for column in layout.columns:
        if column.key == "code":
            cells.append(RenderedCell(kind="text", value=str(total.get("code", "total"))))
        elif column.key == "description":
            cells.append(RenderedCell(kind="text", value=str(total.get("description", "Total"))))
        elif column.key == "value":
            cells.append(_resolve_cell(column, total, layout.section_code))
        else:
            cells.append(RenderedCell(kind=column.kind, value=None))
    return RenderedRow(cells=tuple(cells), is_total=True)


def format_cell(cell: RenderedCell) -> str:
    """Display formatting shared by the CSV metadata rows and the PDF tables:
    thousands separators, parenthesised negatives (research §11 sign
    convention), ``Yes``/``No`` booleans (LMTD Table 5 marking)."""
    if cell.value is None:
        return ""
    if cell.kind == "bool":
        return "Yes" if cell.value else "No"
    if isinstance(cell.value, Decimal):
        if cell.kind == "pct":
            return f"{cell.value:,.2f}"
        number = cell.value
        if number == number.to_integral_value():
            number = number.quantize(Decimal("1"))
        if number < 0:
            return f"({-number:,})"
        return f"{number:,}"
    return str(cell.value)


def machine_cell(cell: RenderedCell) -> str:
    """Machine-readable formatting for CSV data cells: plain decimal strings
    (minus sign, no grouping) so downstream parsers never trip on the
    display conventions."""
    if cell.value is None:
        return ""
    if cell.kind == "bool":
        return "true" if cell.value else "false"
    return str(cell.value)


def build_rendered_return(
    template: ReturnTemplate,
    snapshot: dict[str, Any],
    source_runs: list[dict[str, Any]],
    *,
    package_id: str,
    package_version: int,
) -> RenderedReturn:
    """Resolve the template against a ``regulatory-package-v1`` snapshot.

    Raises 409 (``snapshot_section_missing``) when a required section is
    absent — the snapshot is immutable, so the caller must regenerate.
    """
    sections_by_code = {section.get("code"): section for section in snapshot.get("sections", [])}
    rendered_sections: list[RenderedSection] = []
    for layout in template.sections:
        section = sections_by_code.get(layout.section_code)
        if section is None:
            if layout.optional:
                continue
            raise _missing_section_error(template.template_id, layout.section_code)
        rows = tuple(
            RenderedRow(
                cells=tuple(
                    _resolve_cell(column, row, layout.section_code) for column in layout.columns
                )
            )
            for row in section.get("rows", [])
        )
        rendered_sections.append(
            RenderedSection(
                layout=layout,
                title=layout.sheet_title,
                rows=rows,
                total_row=_resolve_total_row(layout, section),
            )
        )

    institution = snapshot.get("institution", {})
    period = snapshot.get("reporting_period", {})
    metadata = snapshot.get("metadata", {})
    metadata_pairs: tuple[tuple[str, str], ...] = (
        ("Return", f"{template.return_code} — {template.title}"),
        ("Directive citation", template.source_citation),
        ("Regulator", str(snapshot.get("regulator", "BOG"))),
        ("Institution", str(institution.get("name", ""))),
        (
            "Institution code",
            f"{institution.get('short_name', '')} (internal — the ORASS institution-code "
            "scheme is not public, research gap G9)",
        ),
        ("Jurisdiction", str(institution.get("jurisdiction_code", ""))),
        ("Licence type", str(institution.get("license_type", ""))),
        ("Reporting date", str(snapshot.get("reporting_date", ""))),
        ("Reporting period", str(period.get("label", ""))),
        ("Reporting basis", template.basis),
        ("Currency unit", template.currency_unit),
        (
            "Sign convention",
            "Negative amounts shown in parentheses (research §11, CONFIRMED)",
        ),
        ("Template fidelity", str(snapshot.get("fidelity", template.fidelity))),
        ("Template id", template.template_id),
        ("Package version", str(package_version)),
        ("Generated at", str(metadata.get("generated_at", ""))),
    )

    provenance_runs = tuple(
        (
            str(entry.get("module", "")),
            str(entry.get("run_id", "")),
            str(entry.get("input_hash", "")),
            str(entry.get("engine_version", "")),
        )
        for entry in source_runs
    )
    provenance_lines = (
        f"{GENERATED_BY_LINE} · package {package_id} (version {package_version})",
        f"Snapshot schema: {snapshot.get('schema_version', '')}",
        "Every figure traces to the source calculation runs below "
        "(module · run id · input hash · engine version).",
    )
    return RenderedReturn(
        template=template,
        metadata_pairs=metadata_pairs,
        sections=tuple(rendered_sections),
        provenance_runs=provenance_runs,
        provenance_lines=provenance_lines,
        attestation_lines=template.attestation_lines,
    )


